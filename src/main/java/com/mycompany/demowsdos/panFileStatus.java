    public void fillStatus(Filestatusresponse responseSatatus) {
         
        
        //fill Filds
        
        txtComDat.setText(responseSatatus.getCompletiondate());
        txtFilRecCou.setText(responseSatatus.getFilerecordcount());
        txtImRecCou.setText(responseSatatus.getImporterrorcount());
        txtImpErrCou.setText(responseSatatus.getImporterrorcount());
        txtImpWarCou.setText(responseSatatus.getImportwarningcount());
        txtSchErrCou.setText(responseSatatus.getSchemaerrorcount());
        txtStaDat.setText(responseSatatus.getStartdate());
        //create header   {"","",""}
         //String[] strheader =  new String[]{"Message Type", "Date", "Message"};
         String[] strRecords = new String[3];

        for(FilestatusresponseImportmessage responseMessage : responseSatatus.getImportmessages())
        {
           //fill table   
           strRecords[0] = responseMessage.getMessagetype();
           strRecords[1] = responseMessage.getTimeindex();
           strRecords[2] = responseMessage.getMessage();
           table.addRow(strRecords);
        }
        
            //"aqui va el response" ver por que no permite registro, encabezado
            //table = new DefaultTableModel(strheader, 1);
            tblStatus.setModel(table);
    }
